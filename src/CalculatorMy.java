public class CalculatorMy {

    public int firstNumber;
    public int secondNumber;

    public void wykonajDodawanie(){
        System.out.println("Wynik dodawania : " + (firstNumber + secondNumber)); }

    public void wykonajOdejmowanie(){
        System.out.println("Wynik odejmowania : " + (firstNumber - secondNumber)); }

    public void wykonajMnozenie(){
        System.out.println("Wynik mnozenia : " + (firstNumber * secondNumber)); }

    public void wykonajDzielenia(){
        System.out.println("Wynik dodawania : " + (firstNumber/secondNumber)); }

    public void wykonajModulo(){
        System.out.println("Wynik modulo : " + (firstNumber%secondNumber)); }

}
